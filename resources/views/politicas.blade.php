    <h1>Política de Privacidade</h1>
    <p class="updated-date">Última atualização: 15 de agosto de 2024</p>

    <p>A privacidade dos nossos usuários é uma prioridade para nós. Esta Política de Privacidade descreve como coletamos, usamos, e protegemos suas informações pessoais ao utilizar o [Nome do Aplicativo] ("Aplicativo"). Ao utilizar o Aplicativo, você concorda com a coleta e uso de informações de acordo com esta política.</p>

    <h2>1. Informações que Coletamos</h2>
    <p>Podemos coletar os seguintes tipos de informações:</p>
    <ul>
        <li><strong>Informações Pessoais:</strong> Nome, endereço de e-mail, número de telefone, e outras informações que você fornece ao registrar uma conta ou utilizar o Aplicativo.</li>
        <li><strong>Informações de Uso:</strong> Dados sobre como você utiliza o Aplicativo, como as páginas visitadas, as funcionalidades utilizadas, e o tempo gasto em cada seção.</li>
        <li><strong>Informações de Dispositivo:</strong> Informações técnicas sobre o dispositivo que você usa para acessar o Aplicativo, como modelo do dispositivo, sistema operacional, identificadores únicos de dispositivo, e endereço IP.</li>
    </ul>

    <h2>2. Como Usamos Suas Informações</h2>
    <p>As informações que coletamos são usadas para:</p>
    <ul>
        <li><strong>Operação do Aplicativo:</strong> Para fornecer e melhorar a funcionalidade do Aplicativo, personalizar sua experiência, e oferecer suporte técnico.</li>
        <li><strong>Comunicações:</strong> Para enviar notificações, atualizações, e ofertas relacionadas ao Aplicativo. Você pode optar por não receber essas comunicações a qualquer momento.</li>
        <li><strong>Análise e Pesquisa:</strong> Para entender melhor como os usuários interagem com o Aplicativo e para realizar análises estatísticas e pesquisas de mercado.</li>
    </ul>

    <h2>3. Compartilhamento de Informações</h2>
    <p>Não vendemos, alugamos ou compartilhamos suas informações pessoais com terceiros, exceto nos seguintes casos:</p>
    <ul>
        <li><strong>Fornecedores de Serviços:</strong> Podemos compartilhar informações com fornecedores de serviços que auxiliam na operação do Aplicativo, como provedores de hospedagem e análise de dados. Esses terceiros estão obrigados a manter a confidencialidade das suas informações e usá-las apenas para os fins contratados.</li>
        <li><strong>Cumprimento Legal:</strong> Podemos divulgar informações se exigido por lei, ou se acreditarmos de boa-fé que tal ação é necessária para cumprir uma obrigação legal, proteger nossos direitos ou propriedade, ou para proteger a segurança dos usuários ou do público.</li>
        <li><strong>Fusões e Aquisições:</strong> Se o HACKEANDO CASAS for envolvido em uma fusão, aquisição, ou venda de ativos, suas informações podem ser transferidas como parte dessa transação. Notificaremos os usuários antes que suas informações sejam transferidas e se tornem sujeitas a uma política de privacidade diferente.</li>
    </ul>

    <h2>4. Segurança das Informações</h2>
    <p>Implementamos medidas de segurança adequadas para proteger suas informações contra acesso não autorizado, alteração, divulgação ou destruição. No entanto, nenhum método de transmissão ou armazenamento eletrônico é 100% seguro, e não podemos garantir a segurança absoluta das suas informações.</p>

    <h2>5. Retenção de Dados</h2>
    <p>Reteremos suas informações pessoais apenas pelo tempo necessário para cumprir os propósitos descritos nesta Política de Privacidade, a menos que um período de retenção mais longo seja exigido ou permitido por lei.</p>

    <h2>6. Seus Direitos</h2>
    <p>De acordo com a LGPD, você tem o direito de:</p>
    <ul>
        <li><strong>Acessar suas Informações:</strong> Solicitar uma cópia das informações pessoais que mantemos sobre você.</li>
        <li><strong>Retificar Informações:</strong> Corrigir qualquer informação pessoal incorreta ou incompleta.</li>
        <li><strong>Excluir Informações:</strong> Solicitar a exclusão das suas informações pessoais, sujeito a certas exceções.</li>
        <li><strong>Revogar Consentimento:</strong> Retirar seu consentimento para o processamento de suas informações a qualquer momento.</li>
    </ul>
    <p>Para exercer qualquer um desses direitos, entre em contato conosco através do e-mail [Endereço de E-mail].</p>

    <h2>7. Cookies e Tecnologias Semelhantes</h2>
    <p>O Aplicativo pode usar cookies e tecnologias semelhantes para melhorar sua experiência, personalizar conteúdo, e realizar análises de uso. Você pode configurar seu navegador para recusar cookies, mas isso pode limitar algumas funcionalidades do Aplicativo.</p>

    <h2>8. Alterações nesta Política</h2>
    <p>Podemos atualizar esta Política de Privacidade periodicamente. Qualquer alteração será publicada nesta página, e a data de atualização será revisada. O uso contínuo do Aplicativo após a publicação das alterações constitui sua aceitação das mudanças.</p>

    <h2>9. Contato</h2>
    <p>Se você tiver dúvidas ou preocupações sobre esta Política de Privacidade, entre em contato conosco através do e-mail [Endereço de E-mail] ou pelo telefone [Número de Telefone].</p>
